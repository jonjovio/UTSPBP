package main

import (
	"UTS/controllers"
	"fmt"
	"log"
	"net/http"

	"github.com/gorilla/mux"
)

func main() {
	router := mux.NewRouter()

	router.HandleFunc("/getAllRoom", controllers.GetAllRooms).Methods("GET")
	router.HandleFunc("/getDetailRoom", controllers.GetDetailRoom).Methods("GET")

	http.Handle("/", router)
	fmt.Println("Connected to port 8888")
	log.Println("Connected to port 8888")
	log.Fatal(http.ListenAndServe(":8888", router))

}
